    private void btnBuscarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEActionPerformed
        // TODO add your handling code here:
        String n = txtNumABuscar.getText();
        int nE = Integer.parseInt(n);

        if (oper.BusquedaSecuencial(nE) == -1) {
            txtElementoEncontrado.setText("El numero " + n + " no existe en el arreglo");
        } else {
            txtElementoEncontrado.setText("El numero " + n + " esta en la posicion " + (String.valueOf(oper.BusquedaSecuencial(nE))));
        }
    }//GEN-LAST:event_btnBuscarEActionPerformed

