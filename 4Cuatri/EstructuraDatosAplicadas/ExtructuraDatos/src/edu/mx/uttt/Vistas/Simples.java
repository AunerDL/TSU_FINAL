    private void btnInsertarPosicionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarPosicionesActionPerformed
        // TODO add your handling code here:
        
        String Posicion = JOptionPane.showInputDialog("Ingresa la posicion");
        String Numero = JOptionPane.showInputDialog("Ingresa un elemento");

        if (Posicion == null || Numero == null) {
            return;
        }

        int posicion;
        try {
            posicion = Integer.parseInt(Posicion);
        } catch (NumberFormatException e) {
            System.out.println("La posicion debe ser entero.");
            return;
        }

        listas.insertarEnPosicion(Numero, posicion); 
        txtResultados.setText(listas.Imprimir());
    }//GEN-LAST:event_btnInsertarPosicionesActionPerformed

