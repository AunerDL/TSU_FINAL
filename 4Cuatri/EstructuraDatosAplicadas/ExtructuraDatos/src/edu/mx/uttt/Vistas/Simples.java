    private void btnSecuencialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSecuencialActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog("Ingresa el elemento para buscar:");

        if (input != null) {
            NodoSimples resultado = listas.busquedaSecuencial(input);
            if (resultado != null) {
                txtResulBusquedas.setText("Busqueda Secuencial:\n" + resultado.getDato());
            } else {
                txtResulBusquedas.setText("Error");
            }
        }
        
        listas.ordenamientoBurbuja();
        txtResultados.setText(listas.Imprimir());
    }//GEN-LAST:event_btnSecuencialActionPerformed

