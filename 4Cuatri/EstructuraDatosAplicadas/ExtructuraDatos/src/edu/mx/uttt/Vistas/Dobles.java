    private void btnBinarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBinarioActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog("Busqueda Binaria\nIngresa el elemento para buscar:");

        if (input != null) {
            NodoDobles resultado = lista.busquedaBinaria(input);
            if (resultado != null) {
                txtRBusquedas.setText("Busqueda Binaria:\n" + resultado.getDato());
            } else {
                txtRBusquedas.setText("Error en la busqueda");
            }
        }
        lista.ordenamientoBurbuja();
        txtResultados.setText(lista.Imprimir());
    }//GEN-LAST:event_btnBinarioActionPerformed

