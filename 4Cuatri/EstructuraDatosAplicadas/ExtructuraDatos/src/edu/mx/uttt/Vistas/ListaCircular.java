    private void btnInsertarFrentreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarFrentreActionPerformed
        // TODO add your handling code here:
        boolean sentinel = true;
        while (sentinel) {
            String input = JOptionPane.showInputDialog("Ingresa un numero o igresa S para salir\n");

            if (input.equalsIgnoreCase("s") || input.equalsIgnoreCase("S")) {
                sentinel = false;
            } else {
                int elemento;
                try {
                    elemento = Integer.parseInt(input);
                    obj.insertarEnfrente(elemento);
                } catch (NumberFormatException e) {
                    System.out.println("Ingresa un número válido.");
                }
            }
        }

        txtResultados.setText(obj.imprimir());
    }//GEN-LAST:event_btnInsertarFrentreActionPerformed

