    private void btnPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosicionActionPerformed
        // TODO add your handling code here:
        String Posicion = JOptionPane.showInputDialog("Ingresa la posicion");
        String Numero = JOptionPane.showInputDialog("Ingresa un elemento");
        if (Posicion == null || Numero == null) {
            return;
        }

        int posicion;
        int numero;
        try {
            numero = Integer.parseInt(Numero);
            posicion = Integer.parseInt(Posicion);
        } catch (NumberFormatException e) {
            System.out.println("La posicion debe ser entero.");
            return;
        }

        obj.insertarEnPosicion(numero, posicion); 
        txtResultados.setText(obj.imprimir());
    }//GEN-LAST:event_btnPosicionActionPerformed

