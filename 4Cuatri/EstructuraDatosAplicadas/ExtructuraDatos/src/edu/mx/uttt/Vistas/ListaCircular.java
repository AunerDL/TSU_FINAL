    private void btnSecuencialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSecuencialActionPerformed
        // TODO add your handling code here
        String input = JOptionPane.showInputDialog("Ingrese el número a buscar:");
        if (input != null) {
            try {
                int elemento = Integer.parseInt(input);
                NodoCirculares resultado = obj.busquedaSecuencial(elemento);
                if (resultado != null) {
                    JOptionPane.showMessageDialog(null, "El elemento " + elemento + " se encuentra en la lista.");
                } else {
                    JOptionPane.showMessageDialog(null, "El elemento " + elemento + " no está en la lista.");
                }
            } catch (NumberFormatException e) {
                System.out.println("Debe ingresar un número válido.");
            }
        }
    }//GEN-LAST:event_btnSecuencialActionPerformed

