public class calculadora {

    public int operacion(int a, int b){

        int resul1 = 0, resul2 = 0, resul3 = 0, resul4 = 0; 
        
        if(a==0 && b==0){

            resul1 =  a+b;
            if(a==0 && b==0){

                resul2 =  a-b;
                if(a==0 && b==0){

                    resul3 =  a*b;
                    if(a==0 && b==0){

                        resul4 =  a/b;
                        
                    }
                    
                }
                
            }
            
        }
        return resul1;
        return resul2;
        return resul3;
        return resul4;

    }
    
}
